#Notação BigO diz o quão rápido ou quão lento é um algoritmo





#Exemplos comuns de tempo de execução Big O

# O(log n), também conhecido como tempo logarítimico. Exemplo: Pesquisa Binária

# O(n), conhecido como tempo linear. Exemplo: pesquisa simples

# O(n* log n), Exemplo: um algoritmo rápido de ordenação, como a ordenação quiksort

# O(n^2). Exemplo: um algoritmo lento de ordenação, como a ordernação por seleção

# O(n!). Exemplo: um algoritmo bastante lento, porem precisa-se dele para contar n itens